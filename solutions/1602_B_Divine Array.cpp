void solve()
{
    int n; cin>>n; vector<int> a(n); in(a);
    map<int,int> cnt; for(auto x:a) cnt[x]++;
    vector<vector<int>> v; v.push_back(a);
    while(1) {
        for(auto &x:a) {
            x=cnt[x];
        }
        v.push_back(a);
        cnt.clear();
        for(auto x:a) {
            cnt[x]++;
        }
        int f=0;
        for(auto it:cnt) {
            if(it.first!=it.second) {
                f=1; break;
            }
        }
        if(f==0) break; 
    }

    int q; cin>>q;
    while(q--) {
        int x,k; cin>>x>>k;
        if(v.size()<=k) k=v.size()-1;
        cout<<v[k][x-1]<<endl;
    }
}

